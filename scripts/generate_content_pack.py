def base_dest(
    category: str,
    title: str,
    slug: str,
    description: str,
    location: str,
    county: str,
    region: str,
    nearest_town: str,
    distance_km: int,
    travel_time: str,
    best_time: str,
    family_friendly: bool,
    image: str,
    highlights: list[str],
    map_query: str,
    budget: dict,
    directions: str,
    public_transport: str,
    road_conditions: str,
    safety: str,
    carry: str,
    tips: str,
    extra: dict | None = None,
    featured: bool = False,
    featured_order: int = 0,
    trending: bool = False,
    trending_tag: str = "",
    trending_order: int = 0,
) -> dict:
    row = {k: "" for k in DEST_HEADER}
    row.update(
        {
            "category": category,
            "title": title,
            "slug": slug,
            "status": "published",
            "description": description,
            "location": location,
            "county": county,
            "region": region,
            "nearest_town": nearest_town,
            "distance_from_nairobi_km": distance_km,
            "travel_time_from_nairobi": travel_time,
            "best_time_to_visit": best_time,
            "family_friendly": str(family_friendly).lower(),
            "image": image,
            "gallery": "\n".join(
                [
                    image.replace("w=1600", "w=1200"),
                    image.replace("w=1600", "w=1200").replace("q=80", "q=75"),
                ]
            ),
            "highlights": "\n".join(highlights),
            "map_query": map_query,
            "budget_transport": budget.get("transport", 0),
            "budget_accommodation": budget.get("accommodation", 0),
            "budget_entry_fee": budget.get("entry", 0),
            "budget_guide_fee": budget.get("guide", 0),
            "budget_food": budget.get("food", 0),
            "budget_activity": budget.get("activity", 0),
            "directions": directions,
            "public_transport": public_transport,
            "road_conditions": road_conditions,
            "safety_and_conditions": safety,
            "what_to_carry": carry,
            "travel_tips": tips,
            "featured_on_home": str(featured).lower(),
            "featured_sort_order": featured_order,
            "trending_on_home": str(trending).lower(),
            "trending_tag": trending_tag,
            "trending_sort_order": trending_order,
        }
    )
    if extra:
        row.update(extra)
    return row


def card_from_dest(dest: dict, card_id: str, bullets: list[str], budget_label: str, sort_order: int, trail_id: str = "") -> dict:
    return {
        "id": card_id,
        "categoryId": dest["category"],
        "title": dest["title"],
        "location": dest["nearest_town"],
        "budget": budget_label,
        "description": "\n".join(bullets),
        "image": dest["image"].replace("w=1600", "w=900"),
        "map_query": dest["map_query"],
        "slug": dest["slug"],
        "trail_id": trail_id,
        "status": "published",
        "sort_order": sort_order,
    }


def budget_label(dest: dict) -> str:
    day = (
        int(dest["budget_transport"] or 0)
        + int(dest["budget_entry_fee"] or 0)
        + int(dest["budget_guide_fee"] or 0)
        + int(dest["budget_food"] or 0)
        + int(dest["budget_activity"] or 0)
    )
    acc = int(dest["budget_accommodation"] or 0)
    if acc:
        return f"From KES {day:,} day trip · KES {acc:,}/night"
    return f"From KES {day:,} day trip"


